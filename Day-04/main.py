# **Exercise:** Create a list of 5 cities and add another city to it.

cities:list = ["Karachi" , "Islamabad" , "Multan" , "Lahore" , "Peshawar"]

cities.append("RawalPindi")
print(cities)

cities.pop()
print(cities)


cities.remove("Peshawar")
print(cities)


cities.sort()
print(cities)


cities.reverse()
print(cities)

